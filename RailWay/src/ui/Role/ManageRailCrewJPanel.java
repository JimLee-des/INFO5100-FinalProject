    private void populatetblAc() {
        DefaultTableModel model = (DefaultTableModel) tblAc.getModel();
        model.setRowCount(0);
        for (RailCompany rc : system.getRailCompanyDirectory().getRailCompanyList()) {
            if (rc.getRailCompanyName().equals(userAccount.getName())) {
                for (RailCrew crew : rc.getRailCrewList()) {
                    Object[] row = new Object[3];
                    row[0] = crew.getCrewName();
                    for (UserAccount user : system.getUserAccountDirectory().getUserAccountList()) {
                        if (user.getName() == null) {
                            continue;
                        } else if ("Business.Role.RailCrewRole".equals(user.getRole().toString())
                                && user.getName().equals(crew.getCrewName())) {
                            row[1] = user.getUsername();
                            row[2] = user.getPassword();
                        }
                    }
                    model.addRow(row);
                }
            }
        }
    }
