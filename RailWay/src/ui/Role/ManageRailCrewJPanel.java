    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        String crewName = txtName.getText();
        String userName = txtUsername.getText();
        String password = txtPwd.getText();
        
        if(crewName.length() == 0 || userName.length() == 0 || password.length() == 0){
            JOptionPane.showMessageDialog(null, "Please check the blank space");
            return;
        }
        if (!crewName.matches("[a-zA-Z]+")){
            JOptionPane.showMessageDialog(null, "Please check the input of staff name");
            return;
        }
        if (system.getUserAccountDirectory().checkIfUsernameIsUnique(userName) == false) {
            JOptionPane.showMessageDialog(this, "It is already existed.");
        } else {
            UserAccount crewUA = system.getUserAccountDirectory().createUserAccount(crewName, userName, password, new RailCrewRole());
            for (RailCompany rc : system.getRailCompanyDirectory().getRailCompanyList()) {
                if (rc.getRailCompanyName().equals(userAccount.getName())) {
                    rc.addRailCrew(crewName, crewUA);

                }
            }
            populatetblAc();
            txtName.setText("");
            txtUsername.setText("");
            txtPwd.setText("");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAc.getSelectedRow();
        if (selectedRow >= 0) {
            String crewName = (String) tblAc.getValueAt(selectedRow, 0);
            String username = (String) tblAc.getValueAt(selectedRow, 1);
            String password = (String) tblAc.getValueAt(selectedRow, 2);
            UserAccount user = system.getUserAccountDirectory().authenticateUser(username, password);
            system.getUserAccountDirectory().deleteUserAccount(user);
            for (RailCompany rc : system.getRailCompanyDirectory().getRailCompanyList()) {
                if (rc.getRailCompanyName().equals(userAccount.getName())) {
                    rc.deleteRailCrew(crewName);;
                }
            }
            populatetblAc();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

